def movingAverager(data, width):
	"""
	calculate the moving average of a given signal
	@param data a list of floats or a numpy array
	@param width the length of the moving averager
	@return the filtered signal
	"""
	sum = 0
	size = len(data)
	dataOut = numpy.zeros(size)
	halfWidth = int(width / 2)
	iTmp = width - halfWidth
	for i in range(iTmp):
		sum += data[i]
	for i in range(size):
		iTmp = i - halfWidth
		if iTmp >= 0:
			sum -= data[iTmp]
		iTmp += width
		if iTmp < size:
			sum += data[iTmp]
		dataOut[i] = sum / float(width)
	return dataOut

