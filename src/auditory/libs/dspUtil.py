def getAbsMax(data):
	""" 
	find the absolute maximum of a list or a numpy array. NaN and Inf 
		are ignored
	@return the found absolute maximum
	"""
	n = len(data)
	valMin = None
	valMax = None
	for i in range(n):
		x = data[i]
		if (not math.isnan(x)) and (not math.isinf(x)):
			if valMin is None:
				valMin = x
				valMax = x
			else:
				if x < valMin: valMin = x
				if x > valMax: valMax = x
	absMax = valMax
	if valMin < 0:
		if abs(valMin) > valMax:
			absMax = abs(valMin)
	return absMax
		
